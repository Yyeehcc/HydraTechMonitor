def nearest_district(lat, lon):
    best, bd = 'Тараз', 1e9
    for name, (la, lo) in DISTRICT_CENTROIDS.items():
        d = (la - lat) ** 2 + (lo - lon) ** 2
        if d < bd:
            bd, best = d, name
    return best

