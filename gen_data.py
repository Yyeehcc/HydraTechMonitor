# Real river polylines + oblast boundary (from geoBoundaries + OpenStreetMap).
# Generated by process_rivers.py — single source of truth shared with app.js.
with open(os.path.join(BASE, 'rivers_data.json'), encoding='utf-8') as f:
    _geo = json.load(f)
RIVER_LINES = _geo['rivers']          # name -> [[lat,lon], ...]  (real coordinates)
BOUNDARY = _geo['boundary']           # [[lat,lon], ...]          (real oblast outline)

def in_boundary(lat, lon):
    inside = False
    n = len(BOUNDARY); j = n - 1
    for i in range(n):
        yi, xi = BOUNDARY[i]; yj, xj = BOUNDARY[j]
        if ((xi > lon) != (xj > lon)) and \
           (lat < (yj - yi) * (lon - xi) / ((xj - xi) or 1e-12) + yi):
            inside = not inside
        j = i
    return inside

def point_along_source(src, t):
    """Point at fraction t along the real river line, nudged slightly off the
    centerline but always kept INSIDE the oblast boundary (no desert points)."""
    line = RIVER_LINES.get(src)
    if not line or len(line) < 2:
        return None
    seg = t * (len(line) - 1)
    i = min(int(seg), len(line) - 2)
    f = seg - i
    (la1, lo1), (la2, lo2) = line[i], line[i + 1]
    la = la1 + (la2 - la1) * f
    lo = lo1 + (lo2 - lo1) * f
    dla, dlo = la2 - la1, lo2 - lo1
    length = (dla * dla + dlo * dlo) ** 0.5 or 1
    pla, plo = -dlo / length, dla / length
    # try a few small offsets, accept the first that stays inside the boundary
    for off_scale in (0.012, 0.006, 0.0):
        off = (random.random() - 0.5) * off_scale
        nla = la + pla * off
        nlo = lo + plo * off
        if in_boundary(nla, nlo):
            return round(nla, 5), round(nlo, 5)
    return round(la, 5), round(lo, 5)

