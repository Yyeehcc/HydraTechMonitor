for i, c in enumerate(canals):
    c['id'] = i + 1                       # guarantee unique ids (source had dupes)
    # Assign source from the weighted cycle so all rivers get filled evenly
    src, t_lo, t_hi = SOURCE_CYCLE[i % len(SOURCE_CYCLE)]
    c['source'] = src
    # Place the object ALONG its real water source (spread over the t-range)
    t = t_lo + random.random() * (t_hi - t_lo)
    pt = point_along_source(src, t)
    c['lat'], c['lon'] = pt
    # District = geographically nearest centroid, so the label matches location
    c['zone'] = nearest_district(pt[0], pt[1])

    age = 2026 - c['year'] if c['year'] else 50
    kpd_loss = max(0, c['kpd_proj'] - c['kpd_fact']) / max(c['kpd_proj'], 0.01)
    risk = min(100, int(age*0.3 + c['wear']*30 + kpd_loss*40 + (20 if c['condition']=='не удов.' else 0)))
    c['risk_score'] = risk
    c['status'] = 'critical' if risk>=70 or c['condition']=='не удов.' else 'warning' if risk>=40 else 'ok'
    c['status_label'] = {'critical':'Требует ремонта','warning':'Требует осмотра','ok':'Норма'}[c['status']]
    c['inspection_months'] = 3 if c['status']=='critical' else 6 if c['status']=='warning' else 12

    defect_pool = DEFECT_CATALOG[c['status']]
    c['defect'] = defect_pool[i % len(defect_pool)]

    has_fault = random.random() < 0.32
    sensor_fault = random.choice(SENSOR_FAULTS) if has_fault else None

    base_flow = max(0.05, c['capacity'] * c['kpd_fact'] * random.uniform(0.6,0.95)) if c['capacity'] else random.uniform(0.1,1.2)
    base_temp = random.uniform(10.0, 24.0)
    history = [{'h':h,'f':round(max(0,base_flow+(random.random()-0.5)*0.15),3),'t':round(base_temp+(random.random()-0.5)*1.5,1)} for h in range(12)]

    flow = round(max(0, base_flow + random.uniform(-0.1,0.1)), 3)
    temp = round(base_temp + random.uniform(-0.5,0.5), 1)
    level = round(random.uniform(0.5,1.9), 2)
    turb = round(random.uniform(3,40), 1)
    ph = round(random.uniform(6.8,8.2), 1)
    cond = int(random.uniform(200,900))
    online = not (sensor_fault and sensor_fault['code']=='SF06')

    alerts = []
    if level > 1.7: alerts.append({'type':'critical','msg':'Критический уровень воды'})
    if turb > 30: alerts.append({'type':'warning','msg':'Высокая мутность'})
    if temp > 22: alerts.append({'type':'info','msg':'Повышенная температура воды'})
    if not online: alerts.append({'type':'critical','msg':'Датчик не в сети'})
    if sensor_fault:
        prio = sensor_fault['priority']
        t = 'critical' if prio=='высокий' else 'warning'
        alerts.append({'type':t,'msg':f"Неисправность: {sensor_fault['fault']}"})

    c['sensors'] = {
        'flow_speed': flow, 'temperature': temp, 'water_level': level,
        'turbidity': turb, 'ph': ph, 'conductivity': cond,
        'online': online, 'battery': random.randint(15,100),
        'last_update': f'2026-06-25 {random.randint(8,23):02d}:{random.randint(0,59):02d}',
        'history': history, 'alerts': alerts, 'fault': sensor_fault
    }

